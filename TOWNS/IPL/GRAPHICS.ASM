				PUSH	ES
				PUSH	DI

				MOV		AX,0C000H
				MOV		ES,AX

				MOV		SI,OFFSET AOMORI_BMP
				MOV		DI,64*80+(640-64)/8	; (64,576)

				MOV		BX,64
DRAW_AOMORI_Y_LOOP:
				MOV		CX,4
DRAW_AOMORI_X_LOOP:
				LODSW
				XOR		AX,0FFFFH
				STOSW
				LOOP	DRAW_AOMORI_X_LOOP
				SUB		DI,88
				DEC		BX
				JNE		DRAW_AOMORI_Y_LOOP

				POP		DI
				POP		ES
				RET
