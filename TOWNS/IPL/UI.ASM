PROMPT_CD_CHANGE		PROC

						CALL	CLEAR_FIVE_BELOW

						MOV		AX,0105H
						CALL	LOCATE
						MOV		SI,OFFSET CHANGE_CD_MESSAGE1
						CALL	PRINT_TALL

						CALL	WAIT_BUTTON_RELEASE

						MOV		AX,0105H
						CALL	LOCATE
						MOV		SI,OFFSET CHANGE_CD_MESSAGE2
						CALL	PRINT_TALL

						CALL	DEMOSPLASH_MESSAGE

						CALL	WAIT_BUTTON_PRESS

						RET
PROMPT_CD_CHANGE		ENDP

CHANGE_CD_MESSAGE1		DB		"RELEASE GAMEPAD AND MOUSE BUTTONS",0
CHANGE_CD_MESSAGE2		DB		"INSERT TOWNS-APP CD AND PRESS GAMEPAD OR MOUSE BUTTON",0
