int ApplyPatch(
    int nByte,unsigned char byteData[],
    int nPatchFrom,const unsigned char patchFrom[],
    int nPatchTo,const unsigned char patchTo[])
{
	int nOccurrence=0;
	for(int i=0; i<nByte; ++i)
	{
		int found=1;
		for(int j=0; j<nPatchFrom && i+j<nByte; ++j)
		{
			if(byteData[i+j]!=patchFrom[j])
			{
				found=0;
				break;
			}
		}
		if(0!=found)
		{
			printf("Apply Patch at %08x\n",i);
			for(int j=0; j<nPatchTo; ++j)
			{
				byteData[i+j]=patchTo[j];
			}
			++nOccurrence;
		}
	}
	return nOccurrence;
}


int main(int ac,char *av[])
{
	if(3!=ac)
	{
		fprintf(stderr,"Usage: Patch source-file-name destination-file-name\n");
		return 1;
	}


	int nByte=0,nByteRead=0,nByteWritten=0;
	unsigned char *byteData=NULL;
	FILE *fp;

	fp=fopen(av[1],"rb");
	if(NULL==fp)
	{
		fprintf(stderr,"Cannot Open Input File!\n");
		return 1;
	}

	fseek(fp,0,SEEK_END);
	nByte=ftell(fp);
	fseek(fp,0,SEEK_SET);

	byteData=(unsigned char *)malloc(nByte);
	if(NULL==byteData)
	{
		fprintf(stderr,"Not Enough Memory Space!\n");
		return 1;
	}
	nByteRead=fread(byteData,1,nByte,fp);
	fclose(fp);

	if(nByteRead!=nByte)
	{
		fprintf(stderr,"File Read Error!\n");
		return 1;
	}


	if(0==ApplyPatch(nByte,byteData,sizeof(patchUseCDBIOSFrom),patchUseCDBIOSFrom,sizeof(patchUseCDBIOSTo),patchUseCDBIOSTo))
	{
		fprintf(stderr,"Source Pattern UseCDBIOS Not Found!\n");
		return 1;
	}
	if(0==ApplyPatch(nByte,byteData,sizeof(patchUsePagingFrom),patchUsePagingFrom,sizeof(patchUsePagingTo),patchUsePagingTo))
	{
		fprintf(stderr,"Source Pattern UsePaging Not Found!\n");
		return 1;
	}


	fp=fopen(av[2],"wb");;
	if(NULL==fp)
	{
		fprintf(stderr,"Cannot Open Input File!\n");
		return 1;
	}

	nByteWritten=fwrite(byteData,1,nByte,fp);
	if(nByteWritten!=nByte)
	{
		fprintf(stderr,"File Write Error!\n");
		return 1;
	}
	fclose(fp);

	printf("Patched!\n");
	return 0;
}
